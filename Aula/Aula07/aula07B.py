# Crie um algoritmo que leia um numero e mostre o seu dobro, triplo e raus quadrada.
num1 = int(input('Digite um número: '))
dobro = (num1*2)
triplo =(num1*3)
raiz =(num1**(1/2))
print('O dobro de {} é: {}'.format(num1,dobro))
print('O triplo de {} é: {}'.format(num1,triplo))
print('A raiz quadrada de {} é: {}'.format(num1,raiz))